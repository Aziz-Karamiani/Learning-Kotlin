package main

fun main(args: Array<String>) {
    // Arithmetic
    var a: Int = 5
    val b: Int = 2
    println(a + b)
    println(a - b)
    println(a * b)
    println(a / b)
    println(a % b)

    println(a++)
    println(++a)

    println(a--)
    println(--a)

    a += 1
    println(a)
    a -= 1
    println(a)
    a *= 2
    println(a)
    a /= 2
    println(a)
    a %= 2
    println(a)

}